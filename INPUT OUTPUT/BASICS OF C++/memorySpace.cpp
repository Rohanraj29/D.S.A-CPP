//WAP TO FIND THE MEMORY SPACE OF DATA TYPES.
#include<iostream>
using namespace std;
int main()
{
    cout<<"Size of int data type is :-"<<sizeof(int)<<"bytes"<<endl;
    cout<<"Size of boolean data type is :-"<<sizeof(bool)<<"bytes"<<endl;
    cout<<"Size of float data type is :-"<<sizeof(float)<<"bytes"<<endl;
    return 0;
}